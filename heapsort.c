#include <stdio.h>


static void siftDown(int array[], int start, int end)
{
    int root = start;

    while ((2 * root + 1) <= end) {
        int child = 2 * root + 1;
        int swap = root;

        if (array[swap] < array[child])
            swap = child;

        if ((child + 1 <= end) && array[swap] < array[child+1])
            swap = child + 1;

        if (swap == root) {
            return;
        } else {
            int tmp = array[root];

            array[root] = array[swap];
            array[swap] = tmp;
            root = swap;
        }
    }
}

static void heapify(int array[], int len)
{
    int start = (len - 2) / 2;

    while (start >= 0) {
        siftDown(array, start, len - 1);
        start -= 1;
    }
}

static void heapsort(int array[], int len)
{
    int end;

    heapify(array, len);

    end = len - 1;
    while (end > 0) {
        int tmp = array[0];
        array[0] = array[end];
        array[end] = tmp;

        end = end - 1;
        siftDown(array, 0, end);

    }
}


