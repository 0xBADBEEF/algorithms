#include <stdio.h>

static inline void swap_el(int array[], int a_ind, int b_ind)
{
        int tmp;

        tmp = array[a_ind];
        array[a_ind] = array[b_ind];
        array[b_ind] = tmp;
}

static void bubblesort(int array[], int cnt)
{
        int i;
        int swap;

        while (1) {
                swap = 0;
                for (i = 1; i < cnt - 1; i++) {
                        if (array[i - 1] > array[i]) {
                                swap_el(array, i - 1, i);
                                swap = i;
                        }
                }
                if (swap == 0)
                        break;
        }
}

